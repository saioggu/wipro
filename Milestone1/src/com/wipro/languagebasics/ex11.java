package com.wipro.languagebasics;

public class ex11 {
	public static void main(String a[])
	{
		char c=a[0].charAt(0);
		switch(c)
		{
		case 82:System.out.print("Red");
		    break;
		case 66:System.out.print("Blue");
			break;
		case 71:System.out.print("Green");
			break;
		case 79:System.out.print("Orange");
			break;
		case 89:System.out.print("yellow");
			break;
		case 87:System.out.print("White");
			break;
	   default:System.out.print("Invalid");
	       break;
		}
	
	}

}
