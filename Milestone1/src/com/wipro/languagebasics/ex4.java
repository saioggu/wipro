package com.wipro.languagebasics;

public class ex4 {
	public static void main(String a[])
	{
		int x;
		x=Integer.parseInt(a[0]);
		if(x==0)
		{
			System.out.print("it is zero");
		}
		else if(x>0)
		{
			System.out.print("it is a postive number");
		}
		else
		{
			System.out.print("it is a negative number");
		}
	}

}
